	void Behavior::Update(float dt)
	{
		if (IsStateChanging())
		{
			onExit();
			stateCurr = stateNext;
			onInit();
		}
		onUpdate(dt);
	}

	void Behavior::Read(Stream& stream)
	{
		//stream.PushNode("Behavior");
		stream.Read("StateCurr", stateCurr);
		stream.Read("StateNext", stateNext);
		stream.Read("Timer", timer);
		//stream.PopNode();
	}

